module.exports = {
    plusOps: (firstNumber, secondNumber) => {
        return firstNumber + secondNumber
    },

    minusOps: (firstNumber, secondNumber) => {
        return firstNumber - secondNumber
    },

    divideOps: (firstNumber, secondNumber) => {
        return firstNumber / secondNumber
    },

    multiplyOps: (firstNumber, secondNumber) => {
        return firstNumber * secondNumber
    },

    modulusOps: (firstNumber, secondNumber) => {
        return firstNumber % secondNumber
    }
}